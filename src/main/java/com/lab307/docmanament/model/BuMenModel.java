package com.lab307.docmanament.model;

public class BuMenModel {

    private String bh;
    private String qybh;
    private String mc;
    private String dm;
    private String bmms;
    private int zt;

    public BuMenModel() {

    }

    /**
     * @return the bh
     */
    public String getBh() {
        return bh;
    }

    /**
     * @param bh the bh to set
     */
    public void setBh(String bh) {
        this.bh = bh;
    }

    /**
     * @return the qybh
     */
    public String getQybh() {
        return qybh;
    }

    /**
     * @param qybh the qybh to set
     */
    public void setQybh(String qybh) {
        this.qybh = qybh;
    }

    /**
     * @return the mc
     */
    public String getMc() {
        return mc;
    }

    /**
     * @param mc the mc to set
     */
    public void setMc(String mc) {
        this.mc = mc;
    }

    public String getDm() {
        return dm;
    }

    public void setDm(String dm) {
        this.dm = dm;
    }

    /**
     * @return the bmms
     */
    public String getBmms() {
        return bmms;
    }

    /**
     * @param bmms the bmms to set
     */
    public void setBmms(String bmms) {
        this.bmms = bmms;
    }

    /**
     * @return the zt
     */
    public int getZt() {
        return zt;
    }

    /**
     * @param zt the zt to set
     */
    public void setZt(int zt) {
        this.zt = zt;
    }
}
